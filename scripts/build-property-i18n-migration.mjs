/**
 * Builds `supabase/migrations/` SQL that backfills `description_i18n`
 * and `amenities_detail_i18n`. Must stay aligned with lib/property-i18n.ts hashing.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, "..");
const content = JSON.parse(
  fs.readFileSync(path.join(repoRoot, "data/property-detail-content.json"), "utf8"),
);

function dollarQuote(s, seq) {
  const tag = `pd${seq}`;
  return `$${tag}$${s}$${tag}$`;
}

function pgArray(elements, dollarStart) {
  return `ARRAY[\n${elements.map((e, i) => `  ${dollarQuote(e, dollarStart + i)}::text`).join(",\n")}\n]::text[]`;
}

const locales = ["en", "es", "fr", "ja"];
const tmplCount = content.descriptionTemplates.length;
const tplArrays = locales.map((loc) =>
  pgArray(
    content.descriptionTemplates.map((t) => t[loc]),
    loc.charCodeAt(0) * 1000,
  ),
);

const amenCount = content.amenityPhrases.length;
const amenArrays = locales.map((loc) =>
  pgArray(
    content.amenityPhrases.map((a) => a[loc]),
    loc.charCodeAt(0) * 7000 + 50,
  ),
);

const tplDecls = locales
  .map((loc, i) => `  template_${loc} text[] := ${tplArrays[i]};`)
  .join("\n");
const amenDecls = locales.map((loc, i) => `  amen_${loc} text[] := ${amenArrays[i]};`).join("\n");

const migration = `-- Generated by scripts/build-property-i18n-migration.mjs — re-run script after edits to data/property-detail-content.json
-- Applies multilingual copy + deterministic amenities (${tmplCount} templates, ${amenCount} amenity rows → pick 4 per property).

BEGIN;

ALTER TABLE properties
  ADD COLUMN IF NOT EXISTS description_i18n jsonb NOT NULL DEFAULT '{}'::jsonb,
  ADD COLUMN IF NOT EXISTS amenities_detail_i18n jsonb NOT NULL DEFAULT '{}'::jsonb;

DO $$
DECLARE
  r RECORD;
${tplDecls}
${amenDecls}
  tpl_total int := ${tmplCount};
  amen_pool int := ${amenCount};
  seed_signed bigint;
  seed_u32 bigint;
  tidx_zero int;
  desc_en text;
  desc_es text;
  desc_fr text;
  desc_ja text;
  sal int;
  cand int;
  picked int[];
  json_desc jsonb;
  json_amen jsonb;
  ae text[];
  aa text[];
  af text[];
  aj text[];
BEGIN
  FOR r IN SELECT id, title, location, slug FROM properties
  LOOP
    seed_signed := ('x' || substring(md5(r.slug::text), 1, 8))::bit(32)::bigint;
    seed_u32 := CASE WHEN seed_signed < 0 THEN seed_signed + 4294967296::bigint ELSE seed_signed END;
    tidx_zero := ((seed_u32 % tpl_total::bigint)::int + 1);

    desc_en := replace(replace(template_en[tidx_zero], '{{title}}', r.title), '{{location}}', r.location);
    desc_es := replace(replace(template_es[tidx_zero], '{{title}}', r.title), '{{location}}', r.location);
    desc_fr := replace(replace(template_fr[tidx_zero], '{{title}}', r.title), '{{location}}', r.location);
    desc_ja := replace(replace(template_ja[tidx_zero], '{{title}}', r.title), '{{location}}', r.location);

    json_desc := jsonb_build_object(
      'en', to_jsonb(desc_en),
      'es', to_jsonb(desc_es),
      'fr', to_jsonb(desc_fr),
      'ja', to_jsonb(desc_ja)
    );

    picked := ARRAY[]::int[];
    sal := 0;
    WHILE coalesce(array_length(picked, 1), 0) < 4 LOOP
      seed_signed := ('x' || substring(md5((r.slug::text || ':' || sal::text)), 1, 8))::bit(32)::bigint;
      seed_u32 := CASE WHEN seed_signed < 0 THEN seed_signed + 4294967296::bigint ELSE seed_signed END;
      cand := ((seed_u32 % amen_pool::bigint)::int + 1);

      IF NOT (cand = ANY(picked)) THEN
        picked := array_append(picked, cand);
      END IF;

      sal := sal + 1;
      IF sal > 640 THEN
        RAISE EXCEPTION 'amenity pick stalled for slug %', r.slug;
      END IF;
    END LOOP;

    ae := ARRAY[amen_en[picked[1]], amen_en[picked[2]], amen_en[picked[3]], amen_en[picked[4]]];
    aa := ARRAY[amen_es[picked[1]], amen_es[picked[2]], amen_es[picked[3]], amen_es[picked[4]]];
    af := ARRAY[amen_fr[picked[1]], amen_fr[picked[2]], amen_fr[picked[3]], amen_fr[picked[4]]];
    aj := ARRAY[amen_ja[picked[1]], amen_ja[picked[2]], amen_ja[picked[3]], amen_ja[picked[4]]];

    json_amen := jsonb_build_object(
      'en', to_jsonb(ae),
      'es', to_jsonb(aa),
      'fr', to_jsonb(af),
      'ja', to_jsonb(aj)
    );

    UPDATE properties p
      SET description_i18n = json_desc,
          amenities_detail_i18n = json_amen
    WHERE p.id = r.id;
  END LOOP;
END $$;

COMMIT;
`;

const outDir = path.join(repoRoot, "supabase", "migrations");
fs.mkdirSync(outDir, { recursive: true });

const outfile = path.join(outDir, "20260506163000_property_description_amenities_i18n.sql");
fs.writeFileSync(outfile, migration, "utf8");

console.log("Wrote", outfile);
